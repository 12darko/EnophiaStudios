'use strict';
// UI language is auto-detected from the browser — there is intentionally no
// visible language switcher. `?lang=tr|en` works as a hidden override for testing.
function detectLang() {
  try {
    const forced = new URLSearchParams(location.search).get('lang');
    if (forced === 'tr' || forced === 'en') return forced;
    const langs = navigator.languages && navigator.languages.length
      ? navigator.languages : [navigator.language || 'en'];
    return langs.some(l => String(l).toLowerCase().startsWith('tr')) ? 'tr' : 'en';
  } catch (e) { return 'en'; }
}

